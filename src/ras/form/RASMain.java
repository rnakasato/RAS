    private void btnGeneratePlaylistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGeneratePlaylistActionPerformed
        // TODO add your handling code here:
        VLCPlaylistGenerator vpgs = new VLCPlaylistGenerator();
        String path;
        File f;
        if(tblLista.getSelectedRowCount() <=0 || !tblLista.isFocusOwner()){
            if(tbDetail.getSelectedRowCount() <= 0){
                JOptionPane.showMessageDialog(null,"Selecione uma linha!");
                return;
            }
            path = tbDetail.getValueAt(tbDetail.getSelectedRow(),3).toString();
            f = new File(path);
            
        }else{
            path = tblLista.getValueAt(tblLista.getSelectedRow(),1).toString();

            f = new File(path);
        }
        try {
                vpgs.generatePlaylist(f.getPath(), f.getName());
        } catch (Exception ex) {
                Logger.getLogger(RASMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {     
            java.awt.Desktop.getDesktop().open(new File(vpgs.getPlayListPath()));
        } catch (IOException ex) {
            Logger.getLogger(RASMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnGeneratePlaylistActionPerformed

