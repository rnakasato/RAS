    private void tblListaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblListaKeyPressed
        // TODO add your handling code here:
        int linhaAtual = tblLista.getSelectedRow();
        if(evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER){
            btnOpenFolderActionPerformed(null);          
            if(tblLista.getSelectedRow() >= 0)
                tblLista.setRowSelectionInterval(linhaAtual-1, linhaAtual-1);
        }
    }//GEN-LAST:event_tblListaKeyPressed

