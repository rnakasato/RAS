    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        // TODO add your handling code here:
        ArrayList<Object[]> filterRows;
        btnCarregarFolderActionPerformed(null);
        Object[] obj;
        if(tbDetail.getModel() != null && tbDetail.getRowCount() > 0
                && tblLista.getModel() != null && tblLista.getRowCount() > 0){
            filterRows = new ArrayList();
            for(int i = 0; i < tblLista.getModel().getRowCount(); i++){
                if(tblLista.getModel().getValueAt(i, 0).toString().toUpperCase().contains(txtFiltrar.getText().toUpperCase())){
                     obj = new Object[]{tblLista.getModel().getValueAt(i, 0), tblLista.getModel().getValueAt(i, 1)};
                    filterRows.add(obj);
                }
            }
            dtm.setRowCount(0);
            for(Object[] temp: filterRows){
                dtm.addRow(temp);
            }
            
            filterRows = new ArrayList();
            for(int i = 0; i < tbDetail.getModel().getRowCount(); i++){
                if(tbDetail.getModel().getValueAt(i, 0).toString().toUpperCase().contains(txtFiltrar.getText().toUpperCase())){
                    obj = new Object[]{tbDetail.getModel().getValueAt(i, 0), tbDetail.getModel().getValueAt(i, 1),tbDetail.getModel().getValueAt(i, 2),tbDetail.getModel().getValueAt(i, 3)};
                    filterRows.add(obj);
                }
            }
            lblQtdResultados.setText(String.valueOf(filterRows.size()));
            detModel.setRowCount(0);
            for(Object[] temp: filterRows){
                detModel.addRow(temp);
            }
        }
    }//GEN-LAST:event_btnFiltrarActionPerformed

